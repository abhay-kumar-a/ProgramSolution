package dsabootcamp.bitwiseoperators;

public class LeftAndRightShiftOperator {
    public static void main(String[] args) {
        System.out.println(28<<1);   //a<<b=a*2^b; (double)
        System.out.println(28>>1);// a>>b=a/2*b; (half)

        System.out.println(28<<2);   //a<<b=a*2^b;
        System.out.println(28>>2);// a>>b=a/2*b;
    }
}










































































